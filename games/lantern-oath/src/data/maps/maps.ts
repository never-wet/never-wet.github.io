function createMap(definition: Omit<MapDefinition, "width" | "height">): MapDefinition {
  const width = Math.max(...definition.layout.map((row) => row.length));
  return {
    ...definition,
    width,
    height: definition.layout.length,
    layout: definition.layout.map((row) => row.padEnd(width, ".")),
  };
}

